			   Для создания данного косметического продукта вам понадобятся такие ингредиенты:

Основа для мыла. Можно или купить уже готовую мыльную основу в специальном магазине (она бывает белой или прозрачной) или же использовать брусок детского мыла. Белая основа – это то же детское мыло, но только без запаха. А приобретя прозрачную основу вы сможете приготовить очень интересные мыльные композиции. Если вы новичок в этом деле, рекомендуем вам сначала попробовать свои силы на детском мыле.
Базовые масла – они вступают в реакцию с щелочью, в результате чего образуются соли кислот, которые в них содержатся. К таким маслам относятся оливковое, кокосовое, пальмовое, подсолнечное, масло ши, миндальное, масло авокадо и другие. Часть масел имеет выраженный терапевтический эффект, другие способны изменить фактуру готового продукта или его цвет и придать мылу необходимой консистенции.
Эфирные масла Эфирные масла – являются самыми интересными и поистине «волшебными» ингредиентами мыла. Эфирное масло –это, во-первых, натуральный ароматизатор, кроме того, оно обладает определенными лечебными воздействиями. Благодаря добавлению того или иного масла вы легко придадите продукту конкретных свойств. Так, например, масло чайного дерева поможет справиться с избыточной жирностью кожи и прыщами, апельсиновое – избавит от целлюлита и насытит кожу целым комплексом витаминов, иланг-иланговое – обладает омолаживающими и подтягивающими свойствами, идеально подойдет для разглаживания небольших морщинок.
Красители. Существуют специальные мыльные красители или вы можете заменить их обычными пищевыми.
Придать продукту различных цветов вам помогут: куркума (оранжевого и золотистого), шоколад, кофе, какао (коричневого), паприка (оранжевого), корица (красновато-коричневого), календула (золотистого). Также в качестве красителей можно использовать натуральные соки из фруктов и овощей.

Различные добавкиРазличные добавки. Они придадут вашему мылу дополнительных свойств. В качестве добавок обычно берут сливки, глицерин, мед, сухие цветы, настои трав. Если вашей целью является мыло-скраб, стоит добавить в продукт молотого кофе, овсяных хлопьев, измельченных кусочков грецкого ореха.
Посуда, в которой продукт будет подогреваться на водяной бане.
Формочки. Подойдут и обычные кондитерские или детские емкости из пластика или керамики. Также можно приобрести специальные формочки в магазине.
Спирт – им обрабатывается поверхность формочек.
Подогретое молоко, вода либо отвар, которым разбавляется основа для мыла.

			   
			   
